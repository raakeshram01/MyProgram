package myPractise;

public class SwitchMe {

	public static void main(String[] args) {

		int num = 20;

		switch (num) {

		case 40:
			System.out.println(40);

		case 20:
			System.out.println(20);
			

		case 30:
			System.out.println(30);

		case 60:
			System.out.println(60);

			default:System.out.println("Invalid input");
			System.exit(3);
			
			
		}

		
		
		
	}

}
